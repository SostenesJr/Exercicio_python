number_one = input('Digite o primeiro numero: ')
number_two = input('Digite o segundo numero: ')
number_one_int = int(number_one)
number_two_int = int(number_two)
soma = number_one_int + number_two_int
subtracao = number_one_int - number_two_int
multiplicacao = number_one_int * number_two_int
divisao = number_one_int / number_two_int

print(f'{number_one} + {number_two} = {soma}')
print(f'{number_one} - {number_two} = {subtracao}')
print(f'{number_one} * {number_two} = {multiplicacao}')
print(f'{number_one} / {number_two} = {divisao}')
